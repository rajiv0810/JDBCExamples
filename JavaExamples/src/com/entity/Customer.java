package com.entity;

public class Customer {

	private String currentTimesStamp;
	private String creatUserID;
	private String updateTimesStamp;
	private String updateUserID;
	private String version;
	private String setNumber;
	private String studentID;
	private String firstName;
	private String lastName;

	public Customer() {
		super();
	}

	public String getCurrentTimesStamp() {
		return currentTimesStamp;
	}

	public void setCurrentTimesStamp(String currentTimesStamp) {
		this.currentTimesStamp = currentTimesStamp;
	}

	public String getCreatUserID() {
		return creatUserID;
	}

	public void setCreatUserID(String creatUserID) {
		this.creatUserID = creatUserID;
	}

	public String getUpdateTimesStamp() {
		return updateTimesStamp;
	}

	public void setUpdateTimesStamp(String updateTimesStamp) {
		this.updateTimesStamp = updateTimesStamp;
	}

	public String getUpdateUserID() {
		return updateUserID;
	}

	public void setUpdateUserID(String updateUserID) {
		this.updateUserID = updateUserID;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getSetNumber() {
		return setNumber;
	}

	public void setSetNumber(String setNumber) {
		this.setNumber = setNumber;
	}

	public String getStudentID() {
		return studentID;
	}

	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

}
